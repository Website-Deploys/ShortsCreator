def patterns_for_niche(niche: str) -> list[dict[str, Any]]:
    """Return independent JSON-safe pattern objects for one niche."""

    canonical = normalize_niche(niche)
    identifiers = dict.fromkeys((*COMMON_PATTERN_IDS, *NICHE_PATTERN_IDS[canonical]))
    patterns: list[dict[str, Any]] = []
    for pattern_id in identifiers:
        source = PATTERN_LIBRARY[pattern_id]
        copy = dict(source)
        for field in (
            "applies_to_niches",
            "platforms",
            "evidence_source_ids",
            "match_cues",
            "cues",
        ):
            copy[field] = list(source[field])
        patterns.append(copy)
    return patterns


def pattern_by_id(pattern_id: str) -> dict[str, Any] | None:
    """Return an independent pattern object by id."""

    pattern = PATTERN_LIBRARY.get(pattern_id)
    if pattern is None:
        return None
    copy = dict(pattern)
    for field in (
        "applies_to_niches",
        "platforms",
        "evidence_source_ids",
        "match_cues",
        "cues",
    ):
        copy[field] = list(pattern[field])
    return copy


def patterns_by_category(patterns: list[dict[str, Any]]) -> dict[str, list[dict[str, Any]]]:
    """Group pattern objects into the public snapshot category fields."""

    grouped: dict[str, list[dict[str, Any]]] = {
        "hook": [],
        "storytelling": [],
        "retention": [],
        "ending": [],
        "caption": [],
        "pacing": [],
        "music": [],
        "editing": [],
        "title": [],
        "hashtag": [],
        "risk": [],
    }
    for pattern in patterns:
        category = str(pattern.get("category") or "")
        if category in grouped:
            grouped[category].append(pattern)
    return grouped
