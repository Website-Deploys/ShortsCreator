def normalize_niche(value: str | None) -> str:
    """Return a supported canonical niche name."""

    raw = (value or "").strip().lower().replace("-", "_").replace(" ", "_")
    mapped = NICHE_ALIASES.get(raw, raw)
    return mapped if mapped in SUPPORTED_NICHES else "unknown_mixed"


def _pattern(
    pattern_id: str,
    category: str,
    label: str,
    description: str,
    example_structure: str,
    *,
    cues: tuple[str, ...],
    niches: tuple[str, ...] = (),
    platforms: tuple[str, ...] = ALL_PLATFORMS,
    confidence: float = 0.66,
    safety_notes: str = "Keep every claim faithful to the source material.",
) -> dict[str, Any]:
    return {
        "pattern_id": pattern_id,
        "id": pattern_id,
        "category": category,
        "label": label,
        "description": description,
        "applies_to_niches": list(niches or SUPPORTED_NICHES),
        "platforms": list(platforms),
        "evidence_source_ids": [],
        "confidence": confidence,
        "recency_score": 0.45,
        "safety_notes": safety_notes,
        "example_structure": example_structure,
        "do_not_copy_warning": DO_NOT_COPY_WARNING,
        "match_cues": list(cues),
        "cues": list(cues),
    }


